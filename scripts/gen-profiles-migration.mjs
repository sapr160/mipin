#!/usr/bin/env node
// scripts/gen-profiles-migration.mjs — emit the `profiles` / `profiles_private`
// migration (issue #34) to stdout, with the delegation and sport CHECK-constraint
// value lists GENERATED from the vocabulary source of truth (lib/vocab), never
// hand-maintained. Per ADR 0001 a pushed migration is append-only; this script is
// how the one migration file that ships those constraints was produced, and the
// re-generation is kept honest by e2e/profiles-schema.spec.ts, which imports the
// same arrays and asserts the checked-in SQL still matches them.
//
//   node scripts/gen-profiles-migration.mjs > supabase/migrations/<ts>_profiles.sql
//
// The vocab lives in TypeScript (`as const satisfies …`); rather than depend on a
// TS loader (the local Node is not always type-stripping), we read the two source
// files and pull their `code:` values in array order. The drift guard above is
// what makes that extraction safe: if it ever diverged from the real arrays, the
// spec fails.

import { readFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");

/** The `code` values of a vocab source array, in declaration order. */
function codesFrom(relPath) {
  const src = readFileSync(join(ROOT, relPath), "utf8");
  const array = src.slice(src.indexOf("["), src.lastIndexOf("]") + 1);
  const codes = [...array.matchAll(/code:\s*"([^"]+)"/g)].map((m) => m[1]);
  if (codes.length === 0) throw new Error(`no codes found in ${relPath}`);
  return codes;
}

/** Render codes as a wrapped, single-quoted SQL `in (...)` value list. */
function quotedList(codes, indent) {
  const pad = " ".repeat(indent);
  const lines = [];
  const perLine = 6;
  for (let i = 0; i < codes.length; i += perLine) {
    lines.push(pad + codes.slice(i, i + perLine).map((c) => `'${c}'`).join(", "));
  }
  return lines.join(",\n");
}

const delegations = codesFrom("lib/vocab/delegations.ts");
const disciplines = codesFrom("lib/vocab/disciplines.ts");

const sql = `-- profiles + profiles_private: the two rows that make a signed-in account an
-- Onboarded athlete (spec #29, issue #34, domain term "Onboarded"). A row in
-- \`profiles\` existing IS the Onboarded state the Shell gate admits on. The step-2
-- server action creates both rows in one transaction via public.create_profile().
--
-- GENERATED by scripts/gen-profiles-migration.mjs from lib/vocab — the delegation
-- and sport CHECK value lists below come straight from the vocabulary source
-- arrays (issue #33), so a profile can only store a code the pickers offer. Do
-- not hand-edit those lists; regenerate. e2e/profiles-schema.spec.ts asserts they
-- still match the arrays.
--
-- Split into public vs. private per spec #29: the card data any authenticated
-- athlete may read (feeds, swipe, trade proposals in later clusters) lives in
-- \`profiles\`; everything owner-only (DOB, "show me", consent evidence, first-touch
-- attribution) lives in \`profiles_private\`, readable by nobody but its owner.
-- Both cascade from auth.users per ADR 0004 (a user FK cascades unless it is
-- moderation evidence, which neither is): account deletion takes both rows with it.

-- Public card data. Name-or-nickname, delegation + sport codes (CHECK-guarded
-- against the vocabulary), gender, an optional ~160-char bio, and the profile's
-- language. The name/bio length caps mirror lib/auth/profile-form.ts.
create table public.profiles (
  id uuid primary key references auth.users (id) on delete cascade,
  display_name text not null check (char_length(display_name) between 1 and 50),
  delegation text not null check (
    delegation in (
${quotedList(delegations, 6)}
    )
  ),
  sport text not null check (
    sport in (
${quotedList(disciplines, 6)}
    )
  ),
  gender text not null check (gender in ('woman', 'man', 'nonbinary')),
  bio text check (bio is null or char_length(bio) <= 160),
  locale text not null check (locale in ('es', 'en')),
  created_at timestamptz not null default now()
);

alter table public.profiles enable row level security;

-- Authenticated-read: any signed-in athlete can read any public card — the whole
-- point of a shared marketplace. \`(select auth.uid())\` isn't needed for the read
-- itself, but requiring the \`authenticated\` role keeps anonymous traffic out.
create policy "Authenticated users can read any profile"
  on public.profiles
  for select
  to authenticated
  using (true);

-- Owner-write: an athlete may create and edit only their own card. The id is the
-- auth user id, so \`auth.uid() = id\` is the ownership test for both.
create policy "Owners can insert their own profile"
  on public.profiles
  for insert
  to authenticated
  with check ((select auth.uid()) = id);

create policy "Owners can update their own profile"
  on public.profiles
  for update
  to authenticated
  using ((select auth.uid()) = id)
  with check ((select auth.uid()) = id);

-- Owner-only private data. The DOB the gate accepted, the "show me" audience
-- (default everyone, per issue #4), the two consent timestamps (evidence the
-- checkboxes were ticked), and the first-touch source copied from the cookie.
-- No policy grants any other user a read: this row is invisible cross-account.
create table public.profiles_private (
  id uuid primary key references auth.users (id) on delete cascade,
  dob date not null,
  show_me text not null default 'everyone' check (show_me in ('women', 'men', 'everyone')),
  consent_age_accuracy_at timestamptz not null,
  consent_terms_privacy_at timestamptz not null,
  first_touch_source text check (first_touch_source in ('qr', 'share')),
  created_at timestamptz not null default now()
);

alter table public.profiles_private enable row level security;

-- Owner-only, all three verbs: an account may read and write its own private row
-- and no one else's. There is deliberately no cross-user select policy.
create policy "Owners can read their own private profile"
  on public.profiles_private
  for select
  to authenticated
  using ((select auth.uid()) = id);

create policy "Owners can insert their own private profile"
  on public.profiles_private
  for insert
  to authenticated
  with check ((select auth.uid()) = id);

create policy "Owners can update their own private profile"
  on public.profiles_private
  for update
  to authenticated
  using ((select auth.uid()) = id)
  with check ((select auth.uid()) = id);

-- create_profile: the single transaction behind onboarding step 2. A function
-- body is atomic, so both inserts commit together or not at all — the "one
-- transaction creates both rows" guarantee (spec #29). SECURITY INVOKER (default):
-- the inserts run as the caller, so the owner-write policies above authorize them;
-- the id is taken from auth.uid(), never a parameter, so a caller can only ever
-- create their own rows. Consent timestamps are stamped here with now() (the
-- action validates both checkboxes first); locale and first-touch source are read
-- from the request cookies by the action and passed in. search_path is pinned
-- empty and every name is schema-qualified, the recommended hardening for a
-- database function.
create function public.create_profile(
  p_display_name text,
  p_delegation text,
  p_sport text,
  p_gender text,
  p_bio text,
  p_locale text,
  p_dob date,
  p_show_me text,
  p_first_touch_source text
)
returns void
language plpgsql
security invoker
set search_path = ''
as $$
declare
  v_uid uuid := (select auth.uid());
begin
  if v_uid is null then
    raise exception 'create_profile requires an authenticated caller';
  end if;

  insert into public.profiles (id, display_name, delegation, sport, gender, bio, locale)
  values (v_uid, p_display_name, p_delegation, p_sport, p_gender, p_bio, p_locale);

  insert into public.profiles_private (
    id, dob, show_me, consent_age_accuracy_at, consent_terms_privacy_at, first_touch_source
  )
  values (v_uid, p_dob, p_show_me, now(), now(), p_first_touch_source);
end;
$$;
`;

process.stdout.write(sql);
